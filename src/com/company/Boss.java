package com.company;

public class Boss {
    private int health;
    private int damage;
    private String megapower;

    public int getHealth() {
        return health;
    }

    public int getDamage() {
        return damage;
    }

    public String getMegapower() {
        return megapower;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public void setMegapower(String megapower) {
        this.megapower = megapower;
    }
}
